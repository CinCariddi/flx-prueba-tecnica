SELECT d.DENOMINACION, COUNT(e.ID) AS empleado_cuenta
FROM EMPLEADOS e 
JOIN DEPARTAMENTOS d ON e.DEPARTAMENTO_ID = d.ID
GROUP BY d.DENOMINACION 
HAVING COUNT(e.ID) > 5;

