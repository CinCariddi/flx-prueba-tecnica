SELECT e.NOMBRES, p.PUESTO, l.LOCALIDAD 
FROM EMPLEADOS e
JOIN DEPARTAMENTOS d ON d.ID = e.DEPARTAMENTO_ID
JOIN LOCALIDADES l ON l.ID = d.LOCALIDAD_ID
JOIN PUESTOS p ON p.ID = e.PUESTO_ID
WHERE p.PUESTO = "Soporte";

--3
